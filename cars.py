def print_car_brands():
    """Prints a list of popular car brands."""
    car_brands = ["BMW", "Mercedes", "Audi", "Porsche", "Toyota", "Subaru"]  # List of car brands
    for brand in car_brands:  # Iterate through the list of car brands
        print(brand)  # Print each car brand

# Example usage
print_car_brands()  # Call the function to print the car brands
